# -----------------------------------------
# Python_Basics:
# Functions and scope
# -----------------------------------------
#
# Defining functions:
#   def greet(name):
#       return f"Hello, {name}!"
#
# Default parameters:
#   def greet(name="Guest"):
#       return f"Hello, {name}!"
#
# Calling functions:
#   greet("Austin")
#   greet()
#
# Variable scope:
# - Local variables inside functions.
# - Global variables outside functions.
# - Use 'global' keyword to modify globals inside functions.
#
# Example:
#   count = 0
#   def increment():
#       global count
#       count += 1
#
# Lambda functions (anonymous):
#   square = lambda x: x * x
#
# Best practices:
# - Small focused functions.
# - Meaningful names.
# - Minimize globals.
# - Use docstrings.
#
