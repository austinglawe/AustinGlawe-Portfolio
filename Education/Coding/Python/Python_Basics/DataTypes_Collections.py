# Data types and collections
# -----------------------------------------
#
# Basic types:
#   int, float, str, bool
#
# List (ordered, mutable):
#   fruits = ["apple", "banana", "cherry"]
#   fruits.append("orange")
#   print(fruits[1])  # banana
#
# Tuple (ordered, immutable):
#   coords = (10, 20)
#
# Dictionary (key-value pairs):
#   person = {"name": "Austin", "age": 30}
#   print(person["name"])  # Austin
#
# Set (unique items, unordered):
#   unique_numbers = {1, 2, 3, 2, 1}
#   print(unique_numbers)  # {1, 2, 3}
#
# Iteration:
#   for fruit in fruits:
#       print(fruit)
#
#   for key, value in person.items():
#       print(f"{key}: {value}")
#
# Best practices:
# - Lists for ordered, mutable collections.
# - Tuples when immutability is needed.
# - Dictionaries for key-value data.
# - Sets for uniqueness and membership testing.
