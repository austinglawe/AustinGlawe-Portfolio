letters = ["a", "b", "c", "d"]
print(letters[0]) # returns first item of the list
print(letters[-1]) # returns the last item of the list
print(letters)
letters[0] = "A" # changes the first indexed item of the list
print(letters)
print(letters[0:3])
print(letters[:3])
print(letters[0:])
print(letters[:])
print(letters[::2]) # can use a step by adding a colon
print(letters[::-1]) # returns all items in reverse order
