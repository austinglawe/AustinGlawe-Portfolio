# 3 types of numbers
# Integers
x = 1
# Floats
x = 2.2
# Complex Numbers - a + bi
x = 1 + 2j

# Standard Arithmetic operators
print(10 + 3) # Addition
print(10 - 3) # Subtraction
print(10 * 3) # Mulitplication
print(10 / 3) # Division (floating point number returned - float)
print(10 // 3) # Division (whole number returned - integer)
print(10 % 3) # Modulus (returns a remainder of division)
print(10 ** 3) # Exponent

# There are also augmented operators, for instance
x = 10
x = x + 3
# you can do what is above or you can write the x = x + 3 like this below:
x += 3

