# HourlyData_MultiCity

# ---------------------

# Columns and Units:

# city       : Name of the city (string)

# station_id : Meteostat station identifier (string)

# time       : Observation timestamp (ISO 8601 datetime)

# temp       : Air temperature (°C)

# dwpt       : Dew point temperature (°C)

# rhum       : Relative humidity (%)

# prcp       : Precipitation total over the hour (mm)

# snow       : Snow depth (mm)

# wdir       : Wind direction (degrees)

# wspd       : Wind speed (km/h)

# wpgt       : Peak wind gust during the hour (km/h)

# pres       : Sea-level air pressure (hPa)

# tsun       : Sunshine duration over the hour (minutes)

# coco       : Weather condition code (numeric)

#

# DailyData\_MultiCity

# --------------------

# Columns and Units:

# city       : Name of the city (string)

# station\_id : Meteostat station identifier (string)

# time       : Observation date (ISO 8601 date)

# tavg       : Average temperature for the day (°C)

# tmin       : Minimum temperature for the day (°C)

# tmax       : Maximum temperature for the day (°C)

# prcp       : Total precipitation for the day (mm)

# snow       : Total snowfall for the day (mm)

# wdir       : Average wind direction (degrees)

# wspd       : Average wind speed (km/h)

# wpgt       : Peak wind gust for the day (km/h)

# pres       : Average sea-level air pressure (hPa)

# tsun       : Total sunshine duration for the day (minutes)

# coco       : Weather condition code (numeric)

#

# MonthlyData\_MultiCity

# ----------------------

# Columns and Units:

# city       : Name of the city (string)

# station\_id : Meteostat station identifier (string)

# time       : Observation month (ISO 8601 first day of month)

# tavg       : Average temperature for the month (°C)

# tmin       : Minimum temperature recorded in the month (°C)

# tmax       : Maximum temperature recorded in the month (°C)

# prcp       : Total precipitation for the month (mm)

# snow       : Total snowfall for the month (mm)

# wdir       : Average wind direction (degrees)

# wspd       : Average wind speed (km/h)

# wpgt       : Peak wind gust for the month (km/h)

# pres       : Average sea-level air pressure (hPa)

# tsun       : Total sunshine duration for the month (minutes)

# coco       : Weather condition code (numeric)

#

# AllStations\_MetaData

# ---------------------

# Columns and Units:

# station\_id   : Meteostat station identifier (string)

# station\_name : Station name or location (string)

# country      : ISO 3166-1 alpha-2 country code (string)

# region       : Subdivision code (state/province) (string)

# latitude     : Geographic latitude (decimal degrees)

# longitude    : Geographic longitude (decimal degrees)

